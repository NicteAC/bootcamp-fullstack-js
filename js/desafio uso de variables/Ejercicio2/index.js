var numero1 = prompt("Ingrese un numero mayor a cero");
var numero2 = prompt("Ingrese un numero diferente al anterior y mayor a cero");

var suma = parseInt(numero1) + parseInt(numero2);
alert("La suma de ambos numeros es " + suma);

var resta = parseInt(numero1) - parseInt(numero2);
alert("La resta de ambos numeros es " + resta);

var multiplicacion = parseInt(numero1) * parseInt(numero2);
alert("La multiplicacion de ambos numeros es " + multiplicacion);

var division = parseInt(numero1) / parseInt(numero2);
alert("La division de ambos numeros es " + division);

var modulo = parseInt(numero1) % parseInt(numero2);
alert("El modulo de ambos numeros es " + modulo);
